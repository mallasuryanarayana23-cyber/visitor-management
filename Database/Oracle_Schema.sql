-- --------------------------------------------------------
-- 1. SEQUENCES
-- --------------------------------------------------------
CREATE SEQUENCE SEQ_VMS_USERS START WITH 1 INCREMENT BY 1 NOCACHE;
CREATE SEQUENCE SEQ_VMS_VISITORS START WITH 1 INCREMENT BY 1 NOCACHE;
CREATE SEQUENCE SEQ_VMS_FILE_UPLOADS START WITH 1 INCREMENT BY 1 NOCACHE;
CREATE SEQUENCE SEQ_VMS_CHECKIN_LOG START WITH 1 INCREMENT BY 1 NOCACHE;
CREATE SEQUENCE SEQ_VMS_AUDIT_LOG START WITH 1 INCREMENT BY 1 NOCACHE;
CREATE SEQUENCE SEQ_VMS_MASTER_DEPT START WITH 1 INCREMENT BY 1 NOCACHE;
CREATE SEQUENCE SEQ_VMS_MASTER_IDPROOF START WITH 1 INCREMENT BY 1 NOCACHE;
CREATE SEQUENCE SEQ_VMS_MASTER_HOST START WITH 1 INCREMENT BY 1 NOCACHE;
CREATE SEQUENCE SEQ_VMS_MASTER_GATE START WITH 1 INCREMENT BY 1 NOCACHE;

-- --------------------------------------------------------
-- 2. MASTER TABLES
-- --------------------------------------------------------
CREATE TABLE VMS_MASTER_DEPT (
    DEPT_ID NUMBER PRIMARY KEY,
    DEPT_NAME VARCHAR2(100) NOT NULL,
    IS_ACTIVE NUMBER(1) DEFAULT 1
);

CREATE TABLE VMS_MASTER_IDPROOF (
    IDPROOF_ID NUMBER PRIMARY KEY,
    PROOF_NAME VARCHAR2(100) NOT NULL, -- Aadhar, Passport, DL, VoterID
    IS_ACTIVE NUMBER(1) DEFAULT 1
);

CREATE TABLE VMS_MASTER_HOST (
    HOST_ID NUMBER PRIMARY KEY,
    HOST_NAME VARCHAR2(150) NOT NULL,
    DEPT_ID NUMBER REFERENCES VMS_MASTER_DEPT(DEPT_ID),
    EMAIL VARCHAR2(150),
    MOBILE VARCHAR2(15),
    IS_ACTIVE NUMBER(1) DEFAULT 1
);

CREATE TABLE VMS_MASTER_GATE (
    GATE_ID NUMBER PRIMARY KEY,
    GATE_NUMBER VARCHAR2(50) NOT NULL,
    IS_ACTIVE NUMBER(1) DEFAULT 1
);

-- --------------------------------------------------------
-- 3. CORE TABLES
-- --------------------------------------------------------
CREATE TABLE VMS_USERS (
    USER_ID NUMBER PRIMARY KEY,
    USERNAME VARCHAR2(100) UNIQUE NOT NULL,
    PASSWORD_HASH VARCHAR2(256) NOT NULL, -- SHA256
    FULL_NAME VARCHAR2(150) NOT NULL,
    ROLE VARCHAR2(20) NOT NULL CHECK (ROLE IN ('USER', 'GUARD', 'ADMIN')),
    IS_ACTIVE NUMBER(1) DEFAULT 1,
    CREATED_DATE TIMESTAMP DEFAULT SYSTIMESTAMP
);

CREATE TABLE VMS_VISITORS (
    VISITOR_ID NUMBER PRIMARY KEY,
    VISIT_TOKEN VARCHAR2(50) UNIQUE NOT NULL, -- Format: VMS-YYYYMMDD-XXXX
    FULL_NAME VARCHAR2(150) NOT NULL,
    MOBILE VARCHAR2(15) NOT NULL,
    EMAIL VARCHAR2(150),
    COMPANY_NAME VARCHAR2(150),
    PURPOSE VARCHAR2(500) NOT NULL,
    HOST_ID NUMBER REFERENCES VMS_MASTER_HOST(HOST_ID),
    DEPT_ID NUMBER REFERENCES VMS_MASTER_DEPT(DEPT_ID),
    EXPECTED_DATETIME TIMESTAMP NOT NULL,
    IDPROOF_TYPE_ID NUMBER REFERENCES VMS_MASTER_IDPROOF(IDPROOF_ID),
    IDPROOF_NUMBER VARCHAR2(50) NOT NULL,
    STATUS VARCHAR2(20) DEFAULT 'Pending' CHECK (STATUS IN ('Pending', 'Approved', 'Checked-In', 'Checked-Out', 'Cancelled')),
    REGISTERED_BY NUMBER REFERENCES VMS_USERS(USER_ID),
    CREATED_DATE TIMESTAMP DEFAULT SYSTIMESTAMP
);

CREATE TABLE VMS_FILE_UPLOADS (
    FILE_ID NUMBER PRIMARY KEY,
    VISITOR_ID NUMBER REFERENCES VMS_VISITORS(VISITOR_ID),
    UPLOAD_TYPE VARCHAR2(50) CHECK (UPLOAD_TYPE IN ('Photo', 'IDProof')),
    ORIGINAL_NAME VARCHAR2(255) NOT NULL,
    STORED_NAME VARCHAR2(255) NOT NULL, -- GUID + Ext
    FILE_PATH VARCHAR2(1000) NOT NULL,
    FILE_URL VARCHAR2(1000) NOT NULL,
    FILE_SIZE_BYTES NUMBER,
    MIME_TYPE VARCHAR2(50),
    UPLOADED_DATE TIMESTAMP DEFAULT SYSTIMESTAMP
);

CREATE TABLE VMS_CHECKIN_LOG (
    LOG_ID NUMBER PRIMARY KEY,
    VISITOR_ID NUMBER REFERENCES VMS_VISITORS(VISITOR_ID),
    GATE_ID NUMBER REFERENCES VMS_MASTER_GATE(GATE_ID),
    GUARD_ID NUMBER REFERENCES VMS_USERS(USER_ID),
    CHECKIN_TIME TIMESTAMP NOT NULL,
    CHECKOUT_TIME TIMESTAMP,
    DURATION_MINUTES NUMBER
);

CREATE TABLE VMS_AUDIT_LOG (
    AUDIT_ID NUMBER PRIMARY KEY,
    USER_ID NUMBER REFERENCES VMS_USERS(USER_ID),
    ACTION_TYPE VARCHAR2(100) NOT NULL, -- Login, Register, Check-In, Check-Out, etc.
    ENTITY_NAME VARCHAR2(100),
    ENTITY_ID NUMBER,
    IP_ADDRESS VARCHAR2(50),
    ACTION_TIME TIMESTAMP DEFAULT SYSTIMESTAMP,
    DETAILS CLOB
);

-- --------------------------------------------------------
-- 4. VIEWS
-- --------------------------------------------------------

CREATE OR REPLACE VIEW VW_VISITOR_FULL_DETAIL AS
SELECT 
    v.VISITOR_ID, v.VISIT_TOKEN, v.FULL_NAME, v.MOBILE, v.EMAIL, v.COMPANY_NAME, v.PURPOSE,
    v.EXPECTED_DATETIME, v.STATUS, v.IDPROOF_NUMBER,
    h.HOST_NAME, d.DEPT_NAME, p.PROOF_NAME as IDPROOF_TYPE,
    (SELECT FILE_URL FROM VMS_FILE_UPLOADS f WHERE f.VISITOR_ID = v.VISITOR_ID AND f.UPLOAD_TYPE = 'Photo' AND ROWNUM = 1) AS PHOTO_URL,
    (SELECT FILE_URL FROM VMS_FILE_UPLOADS f WHERE f.VISITOR_ID = v.VISITOR_ID AND f.UPLOAD_TYPE = 'IDProof' AND ROWNUM = 1) AS IDPROOF_URL,
    u.FULL_NAME AS REGISTERED_BY_NAME
FROM VMS_VISITORS v
LEFT JOIN VMS_MASTER_HOST h ON v.HOST_ID = h.HOST_ID
LEFT JOIN VMS_MASTER_DEPT d ON v.DEPT_ID = d.DEPT_ID
LEFT JOIN VMS_MASTER_IDPROOF p ON v.IDPROOF_TYPE_ID = p.IDPROOF_ID
LEFT JOIN VMS_USERS u ON v.REGISTERED_BY = u.USER_ID;

CREATE OR REPLACE VIEW VW_TODAY_VISITORS AS
SELECT * FROM VW_VISITOR_FULL_DETAIL
WHERE TRUNC(EXPECTED_DATETIME) = TRUNC(SYSDATE);

CREATE OR REPLACE VIEW VW_ACTIVE_VISITORS AS
SELECT * FROM VW_VISITOR_FULL_DETAIL
WHERE STATUS = 'Checked-In';

