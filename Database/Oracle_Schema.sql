CREATE OR REPLACE PROCEDURE SP_REGISTER_VISITOR (
    p_FULL_NAME IN VARCHAR2,
    p_MOBILE IN VARCHAR2,
    p_EMAIL IN VARCHAR2,
    p_COMPANY_NAME IN VARCHAR2,
    p_PURPOSE IN VARCHAR2,
    p_HOST_ID IN NUMBER,
    p_DEPT_ID IN NUMBER,
    p_EXPECTED_DATETIME IN TIMESTAMP,
    p_IDPROOF_TYPE_ID IN NUMBER,
    p_IDPROOF_NUMBER IN VARCHAR2,
    p_REGISTERED_BY IN NUMBER,
    p_OUT_VISITOR_ID OUT NUMBER,
    p_OUT_TOKEN OUT VARCHAR2
) AS 
    v_seq NUMBER;
BEGIN
    SELECT SEQ_VMS_VISITORS.NEXTVAL INTO v_seq FROM DUAL;
    
    p_OUT_TOKEN := 'VMS-' || TO_CHAR(SYSDATE, 'YYYYMMDD') || '-' || LPAD(v_seq, 4, '0');
    p_OUT_VISITOR_ID := v_seq;

    INSERT INTO VMS_VISITORS (
        VISITOR_ID, VISIT_TOKEN, FULL_NAME, MOBILE, EMAIL, COMPANY_NAME, PURPOSE, 
        HOST_ID, DEPT_ID, EXPECTED_DATETIME, IDPROOF_TYPE_ID, IDPROOF_NUMBER, REGISTERED_BY
    ) VALUES (
        v_seq, p_OUT_TOKEN, p_FULL_NAME, p_MOBILE, p_EMAIL, p_COMPANY_NAME, p_PURPOSE,
        p_HOST_ID, p_DEPT_ID, p_EXPECTED_DATETIME, p_IDPROOF_TYPE_ID, p_IDPROOF_NUMBER, p_REGISTERED_BY
    );
END SP_REGISTER_VISITOR;
/

