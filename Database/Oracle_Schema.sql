CREATE OR REPLACE PROCEDURE SP_CHECKIN_VISITOR (
    p_VISITOR_ID IN NUMBER,
    p_GATE_ID IN NUMBER,
    p_GUARD_ID IN NUMBER
) AS 
BEGIN
    UPDATE VMS_VISITORS SET STATUS = 'Checked-In' WHERE VISITOR_ID = p_VISITOR_ID;
    
    INSERT INTO VMS_CHECKIN_LOG (
        LOG_ID, VISITOR_ID, GATE_ID, GUARD_ID, CHECKIN_TIME
    ) VALUES (
        SEQ_VMS_CHECKIN_LOG.NEXTVAL, p_VISITOR_ID, p_GATE_ID, p_GUARD_ID, SYSTIMESTAMP
    );
END SP_CHECKIN_VISITOR;
/

CREATE OR REPLACE PROCEDURE SP_CHECKOUT_VISITOR (
    p_VISITOR_ID IN NUMBER
) AS 
    v_log_id NUMBER;
    v_checkin_time TIMESTAMP;
    v_duration NUMBER;
BEGIN
    UPDATE VMS_VISITORS SET STATUS = 'Checked-Out' WHERE VISITOR_ID = p_VISITOR_ID;
    
    -- Find latest check-in
    SELECT LOG_ID, CHECKIN_TIME INTO v_log_id, v_checkin_time 
    FROM VMS_CHECKIN_LOG 
    WHERE VISITOR_ID = p_VISITOR_ID AND CHECKOUT_TIME IS NULL 
    ORDER BY CHECKIN_TIME DESC FETCH FIRST 1 ROWS ONLY;

    -- Update checkout and duration
    v_duration := EXTRACT(DAY FROM (SYSTIMESTAMP - v_checkin_time)) * 24 * 60 + 
                  EXTRACT(HOUR FROM (SYSTIMESTAMP - v_checkin_time)) * 60 + 
                  EXTRACT(MINUTE FROM (SYSTIMESTAMP - v_checkin_time));

    UPDATE VMS_CHECKIN_LOG 
    SET CHECKOUT_TIME = SYSTIMESTAMP, DURATION_MINUTES = v_duration
    WHERE LOG_ID = v_log_id;
END SP_CHECKOUT_VISITOR;
/

CREATE OR REPLACE PROCEDURE SP_GET_DASHBOARD_COUNTS (
    p_TODAY_EXPECTED OUT NUMBER,
    p_CHECKED_IN OUT NUMBER,
    p_CHECKED_OUT OUT NUMBER,
    p_PENDING OUT NUMBER,
    p_MONTH_REGISTERED OUT NUMBER
) AS
BEGIN
    SELECT COUNT(*) INTO p_TODAY_EXPECTED FROM VMS_VISITORS WHERE TRUNC(EXPECTED_DATETIME) = TRUNC(SYSDATE);
    SELECT COUNT(*) INTO p_CHECKED_IN FROM VMS_VISITORS WHERE STATUS = 'Checked-In';
    SELECT COUNT(*) INTO p_CHECKED_OUT FROM VMS_VISITORS WHERE TRUNC(EXPECTED_DATETIME) = TRUNC(SYSDATE) AND STATUS = 'Checked-Out';
    SELECT COUNT(*) INTO p_PENDING FROM VMS_VISITORS WHERE STATUS = 'Pending';
    SELECT COUNT(*) INTO p_MONTH_REGISTERED FROM VMS_VISITORS WHERE TRUNC(CREATED_DATE, 'MM') = TRUNC(SYSDATE, 'MM');
END SP_GET_DASHBOARD_COUNTS;
/

-- Sample Report Procdure (using SYS_REFCURSOR for ADO.NET)
CREATE OR REPLACE PROCEDURE SP_GET_VISITOR_REPORT (
    p_START_DATE IN DATE,
    p_END_DATE IN DATE,
    p_DEPT_ID IN NUMBER,
    p_RECORDSET OUT SYS_REFCURSOR
) AS 
BEGIN
    OPEN p_RECORDSET FOR 
    SELECT * FROM VW_VISITOR_FULL_DETAIL 
    WHERE TRUNC(EXPECTED_DATETIME) >= p_START_DATE 
      AND TRUNC(EXPECTED_DATETIME) <= p_END_DATE
      AND (p_DEPT_ID = 0 OR DEPT_ID = p_DEPT_ID);
END SP_GET_VISITOR_REPORT;
/

CREATE OR REPLACE PROCEDURE SP_AUTHENTICATE_USER (
    p_USERNAME IN VARCHAR2,
    p_PASSWORD_HASH IN VARCHAR2,
    p_RECORDSET OUT SYS_REFCURSOR
) AS 
BEGIN
    OPEN p_RECORDSET FOR 
    SELECT USER_ID, USERNAME, FULL_NAME, ROLE, IS_ACTIVE 
    FROM VMS_USERS 
    WHERE USERNAME = p_USERNAME AND PASSWORD_HASH = p_PASSWORD_HASH;
END SP_AUTHENTICATE_USER;
/

CREATE OR REPLACE PROCEDURE SP_SET_GUARD_PASSWORD (
    p_EMP_NO IN VARCHAR2,
    p_PASSWORD_HASH IN VARCHAR2,
    p_OUT_STATUS OUT NUMBER 
) AS 
    v_count NUMBER;
BEGIN
    SELECT COUNT(*) INTO v_count FROM VMS_USERS WHERE USERNAME = p_EMP_NO AND ROLE = 'GUARD';
    
    IF v_count > 0 THEN
        UPDATE VMS_USERS SET PASSWORD_HASH = p_PASSWORD_HASH WHERE USERNAME = p_EMP_NO AND ROLE = 'GUARD';
        p_OUT_STATUS := 1; -- Success
    ELSE
        -- Optionally auto-create the guard if not exists, but for now we just fail if employee doesn't correspond to a guard account
        p_OUT_STATUS := 0; -- Not Found
    END IF;
END SP_SET_GUARD_PASSWORD;
/

